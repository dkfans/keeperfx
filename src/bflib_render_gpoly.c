    gploc_pt_av   = point_a->V >> 16;
    gploc_pt_bu   = point_b->U >> 16;
    gploc_pt_bv   = point_b->V >> 16;
    gploc_pt_cu   = point_c->U >> 16;
    gploc_pt_cv   = point_c->V >> 16;


