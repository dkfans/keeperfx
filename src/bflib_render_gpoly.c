    long exceeds_window = ((point_a->X | point_b->X | point_c->X) < 0) || (point_a->X > vec_window_width) || (point_b->X > vec_window_width) || (point_c->X > vec_window_width);
    { // Reorder points
        int min_y = point_a->Y;
        struct PolyPoint* point_tmp;
        if (min_y > point_b->Y)
        {
            min_y = point_b->Y;
            point_tmp = point_a;
            point_a = point_b;
            point_b = point_tmp;
        }
        if (min_y > point_c->Y)
        {
            point_tmp = point_a;
            point_a = point_c;
            point_c = point_tmp;
        }
        if (point_b->Y > point_c->Y)
        {
            point_tmp = point_b;
            point_b = point_c;
            point_c = point_tmp;
        }
    }
